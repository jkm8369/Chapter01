package com.javaex.ex01;

public class Ex04 {
	public static void main(String[] args) {
		
		char ch01 = 'A';
		char ch02 = '안';
		
		System.out.println(ch01);
		System.out.println(ch02);
		
		char ch03 = 65;
		System.out.println(ch03);
		
		char ch04 = '3';
		System.out.println(ch04);
		
		/*
		''안에 오는건 무조건 글자, 
		''안에 한글자씩만 쓸 수 있다. 
		*/
		
		char ch05 = '안';           //""는 안된다
		System.out.println(ch05);
						
	}
}
